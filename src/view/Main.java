    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        Cliente cliente = null;
        for (int i = 0; i < lstClientes.size(); i++) {
            if (lstClientes.get(i).getNome().equals(txtNome.getText())) {
                cliente = lstClientes.get(i);
            }
        }
        if (cliente == null) {
            JOptionPane.showMessageDialog(null, "Cliente não cadastrado");
        } else {
            txtIdade.setText(Integer.toString(cliente.getIdade()));
            txtTelefone.setText(cliente.getTelefone());
            txtLimite.setText(cliente.getLimite().toString());
            cmbPais.setSelectedItem(cliente.getPais().getNome() + " - " + cliente.getPais().getSigla());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

