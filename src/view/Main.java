    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if (txtNome.getText().equals("") || txtIdade.getText().equals("") || txtTelefone.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Existem Campos Nulos!");
        } else {
            String ddi = txtTelefone.getText().substring(0, 2);
            boolean teste = false;
            for (int i = 0; i < lstClientes.size(); i++) {
                if (lstClientes.get(i).getNome().equals(txtNome.getText())) {
                    teste = true;
                }
            }
            if (teste) {
                JOptionPane.showMessageDialog(null, "Já existe cliente com esse nome!");
            } else {
                if (txtNome.getText().length() < 5) {
                    JOptionPane.showMessageDialog(null, "Nome deve conter 5 ou mais letras!");
                } else {
                    Cliente cliente = new Cliente();
                    cliente.setIdade(Integer.parseInt(txtIdade.getText()));
                    cliente.setNome(txtNome.getText());
                    cliente.setTelefone(txtTelefone.getText());
                    String[] splitted = cmbPais.getSelectedItem().toString().split(" - ");
                    Pais pais = null;
                    for (int i = 0; i < lstPaises.size(); i++) {
                        if (lstPaises.get(i).getNome().equals(splitted[0])) {
                            pais = lstPaises.get(i);
                        }
                    }
                    if (ddi.equals(pais.getDdi())) {
                        cliente.setPais(pais);
                        lstClientes.add(cliente);
                        cliente.setLimite(LimiteController.testaLimite(cliente));
                        txtLimite.setText(cliente.getLimite().toString());
                        JOptionPane.showMessageDialog(null, "Cliente Cadastrado com Sucesso!\nSeu limite é de R$ " + cliente.getLimite());
                    } else{
                       JOptionPane.showMessageDialog(null, "Telefone Invalido para o "+pais.getNome()); 
                    }
                }
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

